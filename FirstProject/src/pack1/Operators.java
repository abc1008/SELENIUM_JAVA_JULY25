package pack1;

public class Operators {
	
	
	public static void main(String[] args) {
		
//		3.	Increment operator
//			a.	Pre-increment operator

		int x  = 60; 
		System.out.println(x);
		
//		First value of x will be increased by 1 and then it
//		will be assigned to y
		
		int y = ++x;
		
		System.out.println(y);
		
		
//		b.	Post-increment operator
		
		int a = 40;
		System.out.println(a);
		
		
//		First value of a will be assigned to b and then value of a it will be 
//		incremented by 1
//		
//		a = 40
		
		int b = a++;
		
		b = a;  // 41
		
		System.out.println(b);
		System.out.println(a);
		

//			1.	Decrement Operator
//				a.	Pre-decrement operator
		
		
//		First value of t will be decreased by 1 and then it will be assigned to u

		System.out.println();
		
		int t = 50;
		System.out.println(t);
		
		int u = --t;
		System.out.println(u);
		System.out.println(t);
		
//				b.	Post-decrement operator
		System.out.println();
		
		int p = 70;
		System.out.println(p);
		
		
//		First value of p assigned to q and then it will be will be decremented by 1 
		int q = p--;
		
		System.out.println(q);
		System.out.println(p);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
